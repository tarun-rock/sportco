$(document).ready(function(){
  function timeline_adjust(){
    if($(window).width() < 768 ){
      $('.timeline-body').each(function(){
        var ht = $(this).height();
        $(this).parents('li').height(ht);
      });
    }
    else{
      $('.timeline-body').each(function(){
        $(this).parents('li').css('height','auto');
      });
    }
  }
  timeline_adjust();
  $(window).resize(function() {
    timeline_adjust();
  });
});
